import mongoose from "mongoose";

const UserAuthSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const UserAuth = mongoose.model("UserAuth", UserAuthSchema);

export default UserAuth;
